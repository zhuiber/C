// (8) 打印所有的“水仙花数”。所谓“水仙花数”是指一个三位数, 其各位数字的立方和等于该数本身。
// 例如, 153是“水仙花数”, 因为153 = 1^3 + 3^3 + 5^3
// 【设计思想】首先确定水仙花数n可能存在的范围, 因为n是一个三位数, 所以范围确定为n从100变化到999,
// 分离出n的百位i、十位个位k后, 只要判断n是否等于i *i *i + j *j *j + k *k *k即可知道n是否是水仙花数。

#include <stdio.h>

int main()
{
  int i, a, b, c, n = 0;
  for (i = 100; i < 1000; i++)
  {
    a = i / 100;
    b = (i - a * 100) / 10;
    c = i % 10;
    if (a * a * a + b * b * b + c * c * c == i)
    {
      n++;
      printf("%d, %d\n", i, n);
    }
  }
}