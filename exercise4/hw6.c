// (6) 假设从键盘输入从某日午夜零点到现在已经历的时间(单位 : 秒),
// 编写程序计算到现在为止已过了多少天， 现在时间是多少

#include <stdio.h>

int main()
{
  int t, d, h, min, s;
  scanf("%d", &t);
  d = t / (24 * 60 * 60);
  h = (t - d * (24 * 60 * 60)) / (60 * 60);
  min = (t - d * (24 * 60 * 60) - h * (60 * 60)) / (60);
  s = t - d * (24 * 60 * 60) - h * (60 * 60) - min * 60;
  printf("现在的时间是%d天%d小时%d分钟%d秒", d, h, min, s);
  return 0;
}