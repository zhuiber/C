// (1) 编写一程序要求任意输人4位十六进制整数, 以反序的方式输出该十六进制数例如, 输人9AFO, 则输出OFA9。
// 思路：
// 输入4位十六进制数给变量a
// 取a的最低一位十六进制数并左移12位后赋值给b
// 取a的次低一位十六进制数并左移4位后与b相加再赋值给b
// 取a的次高一位十六进制数并右移4位后与b相加再赋值给b
// 取a的最高一位十六进制数并右移12位后与b相加再赋值给b
// 输出变换后的十六进制数

#include <stdio.h>

int main()
{
  short int a, b;
  scanf("%4x", &a);
  b = (a & 0x000f) << 12;
  b += (a & 0x00f0) << 4;
  b += (a & 0x0f00) >> 4;
  b += (a & 0xf000) >> 12;
  printf("%4x\n", b);
  return 0;
}